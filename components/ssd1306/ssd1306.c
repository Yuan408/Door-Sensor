/* 发送命令字节 */
static esp_err_t ssd1306_write_cmd(uint8_t cmd)
{
    /* SSD1306 控制字节：0x00 表示后续字节为命令 */
    uint8_t buf[2] = {0x00, cmd};
    return i2c_master_transmit(g_ssd1306_dev, buf, sizeof(buf), 100);
}

/* 发送多个命令字节（单次 I2C 事务）
 * 必须将所有命令字节打包在一次 I2C 传输中发送，因为 SSD1306 的多字节命令
 * （如 0x20,0x02 设置寻址模式）要求参数紧跟命令码在同一事务中。
 * 若拆分为多次独立事务，下一事务的控制字节 0x00 会被误读为前一命令的参数。 */
static esp_err_t ssd1306_write_cmds(const uint8_t *cmds, size_t len)
{
    /* 控制字节 0x00 + 所有命令字节 = 一次连续传输 */
    uint8_t *buf = malloc(len + 1);
    if (buf == NULL) {
        ESP_LOGE(TAG, "Failed to allocate cmd buffer");
        return ESP_ERR_NO_MEM;
    }
    buf[0] = 0x00;  /* Co=0, D/C#=0: 后续字节均为命令 */
    memcpy(buf + 1, cmds, len);
    esp_err_t ret = i2c_master_transmit(g_ssd1306_dev, buf, len + 1, 500);
    free(buf);
    return ret;
}

uint8_t *ssd1306_get_framebuffer(void)
{
    return g_framebuffer;
}

/* 将 framebuffer 全部刷新到 OLED
 * 分页逐页发送，每页先设置页地址和列地址（打包为单次 I2C 命令事务），
 * 然后将该页 128 字节数据拆为 32 字节小块发送，避免大块传输的兼容性问题。 */
void ssd1306_flush(void)
{
    for (int page = 0; page < SSD1306_PAGES; page++) {
        /* 设置页地址 + 列地址，单次命令事务 */
        uint8_t addr_cmds[] = {0xB0 + page, 0x00, 0x10};
        esp_err_t ret = ssd1306_write_cmds(addr_cmds, sizeof(addr_cmds));
        if (ret != ESP_OK) {
            ESP_LOGE(TAG, "Flush page %d addr failed: %s", page, esp_err_to_name(ret));
            return;
        }

        /* 整页 128 字节一次性发送（与已验证的白屏测试一致） */
        uint8_t buf[129];
        buf[0] = 0x40;
        memcpy(buf + 1, &g_framebuffer[page * SSD1306_WIDTH], SSD1306_WIDTH);
        ret = i2c_master_transmit(g_ssd1306_dev, buf, sizeof(buf), 500);
        if (ret != ESP_OK) {
            ESP_LOGE(TAG, "Flush page %d data failed: %s", page, esp_err_to_name(ret));
            return;
        }
    }
}

/* 初始化 SSD1306 OLED 显示屏（128x64 像素，I2C 地址 0x3C）
 * 首先添加 I2C 从设备，然后发送完整的初始化命令序列：
 *   1. 关闭显示 → 2. 配置寻址模式 → 3. 设置扫描方向
 *   4. 设置 MUX 和偏移 → 5. 配置时钟和电荷泵
 *   6. 设置对比度 → 7. 开启显示
 * 最后清空缓冲区并刷新到屏幕。 */
esp_err_t ssd1306_init(i2c_master_bus_handle_t bus_handle,
                       i2c_master_dev_handle_t *dev_handle)
{
    /* 配置 I2C 从设备 */
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address   = SSD1306_I2C_ADDR,  /* 0x3C，SSD1306 默认地址 */
        .scl_speed_hz    = 100000,
    };

    esp_err_t ret = i2c_master_bus_add_device(bus_handle, &dev_cfg, dev_handle);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to add SSD1306 device: %s", esp_err_to_name(ret));
        return ret;
    }
    g_ssd1306_dev = *dev_handle;  /* 保存全局句柄供后续显示操作使用 */

    /* 先发送软件复位（单独事务），等待复位完成后再发送初始化序列。
     * 必须在独立事务中发送 0xE2，因为 SSD1306 复位期间无法处理后续命令。 */
    ssd1306_write_cmd(0xE2);
    vTaskDelay(pdMS_TO_TICKS(10));

    /* SSD1306 初始化命令序列（页寻址模式）
     * 所有命令在单次 I2C 事务中连续发送，确保多字节命令的参数正确传递。
     * 128x64 分辨率，8 页 × 128 列，逐页写入 */
    const uint8_t init_cmds[] = {
        0xAE,       // 1. Display OFF（配置期间关闭显示）
        0x20, 0x02, // 2. Set Page Addressing Mode（简化地址管理）
        0x40,       // 3. Set Display Start Line = 0
        0xA1,       // 4. Segment Re-map（左右镜像，适配物理接线）
        0xC8,       // 5. COM Scan Direction（上下翻转，适配物理接线）
        0xA8, 0x3F, // 6. Set MUX Ratio = 63（64 行，即 128x64）
        0xD3, 0x00, // 7. Set Display Offset = 0
        0xDA, 0x12, // 8. COM Pins Hardware Configuration
        0xD5, 0x80, // 9. Clock Divide Ratio / Oscillator Frequency
        0xD9, 0xF1, // 10. Pre-charge Period
        0xDB, 0x30, // 11. VCOMH Deselect Level
        0x81, 0xCF, // 12. Contrast Control = 0xCF（高对比度）
        0xA4,       // 13. Display follows RAM content（不强制全亮）
        0xA6,       // 14. Normal Display（非反白模式）
        0x8D, 0x14, // 15. Charge Pump Setting（启用内部电荷泵）
        0xAF,       // 16. Display ON（开启显示）
    };

    ret = ssd1306_write_cmds(init_cmds, sizeof(init_cmds));
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "SSD1306 init command sequence failed");
        return ret;
    }

    ssd1306_clear();
    ssd1306_flush();

    ESP_LOGI(TAG, "SSD1306 initialized successfully");
    return ESP_OK;
}

/* 将 8×8 位图从行优先转置为列优先（SSD1306 GDDRAM 格式）
 * src[8] — 8 行，每行 1 字节（bit 7=最左像素），
 * dst[8] — 8 列，每列 1 字节（bit 0=最顶像素）。 */
static void transpose_8x8(const uint8_t src[8], uint8_t dst[8])
{
    for (int col = 0; col < 8; col++) {
        uint8_t byte = 0;
        for (int row = 0; row < 8; row++) {
            if (src[row] & (1 << (7 - col))) {
                byte |= (1 << row);
            }
        }
        dst[col] = byte;
    }
}

void ssd1306_clear(void)
{
    memset(g_framebuffer, 0x00, sizeof(g_framebuffer));
}

/* 诊断用：填充整个 framebuffer 并刷新 */
void ssd1306_test_fill(uint8_t pattern, uint32_t delay_ms)
{
    memset(g_framebuffer, pattern, sizeof(g_framebuffer));
    ESP_LOGI(TAG, "test_fill: g_framebuffer[0..7] = %02X %02X %02X %02X %02X %02X %02X %02X",
             g_framebuffer[0], g_framebuffer[1], g_framebuffer[2], g_framebuffer[3],
             g_framebuffer[4], g_framebuffer[5], g_framebuffer[6], g_framebuffer[7]);
    ssd1306_flush();
    if (delay_ms > 0) {
        vTaskDelay(pdMS_TO_TICKS(delay_ms));
    }
}

/* 在 OLED 上显示两行字符串（每行最多 16 个字符，8x16 字体）
 * line1 → page 0+1（上半屏，Y=0~15）
 * line2 → page 2+3（下半屏，Y=16~31）
 * 字符映射：font8x16[ascii - 0x20]，超出范围显示空格
 *
 * 注意：font8x16 为行优先格式（16 字节 = 16 行 × 每行 8 像素），
 * 而 SSD1306 GDDRAM 为列优先（每字节 = 1 列 × 8 垂直像素），
 * 因此需要通过 transpose_8x8 将每个字符的 8×8 像素块转置。 */
void ssd1306_display_string(const char *line1, const char *line2)
{
    ssd1306_clear();  /* 先清空 framebuffer */

    uint8_t top[8], bottom[8];  /* 转置后的上半页和下半页数据 */

    /* 第一行：逐字符绘制到 page 0（上半字节）和 page 1（下半字节） */
    for (int col = 0; col < SSD1306_WIDTH; col += 8) {
        int char_idx = col / 8;
        if (line1[char_idx] == '\0') break;  /* 字符串结束，停止绘制 */
        uint8_t ch = (uint8_t)line1[char_idx];
        if (ch < 0x20 || ch > 0x7F) ch = 0x20;  /* 不可打印字符替换为空格 */
        const uint8_t *glyph = font8x16[ch - 0x20];  /* 查字体表 */

        /* 将行优先字体数据转置为 SSD1306 列优先格式 */
        transpose_8x8(glyph, top);       /* 行 0~7 → page 0 的 8 列 */
        transpose_8x8(glyph + 8, bottom); /* 行 8~15 → page 1 的 8 列 */

        memcpy(&g_framebuffer[0 * SSD1306_WIDTH + col], top, 8);
        memcpy(&g_framebuffer[1 * SSD1306_WIDTH + col], bottom, 8);
    }

    /* 第二行：逐字符绘制到 page 2 和 page 3 */
    for (int col = 0; col < SSD1306_WIDTH; col += 8) {
        int char_idx = col / 8;
        if (line2[char_idx] == '\0') break;
        uint8_t ch = (uint8_t)line2[char_idx];
        if (ch < 0x20 || ch > 0x7F) ch = 0x20;
        const uint8_t *glyph = font8x16[ch - 0x20];

        transpose_8x8(glyph, top);
        transpose_8x8(glyph + 8, bottom);

        memcpy(&g_framebuffer[2 * SSD1306_WIDTH + col], top, 8);
        memcpy(&g_framebuffer[3 * SSD1306_WIDTH + col], bottom, 8);
    }

    ssd1306_flush();  /* 将 framebuffer 整体刷新到 OLED 硬件 */
}